package week1.day2;

public class IterationLoop {

	public static void main(String[] args) {
		
			//For loop
			for (int i = 1; i <=10; i++) {
				System.out.println(i);
			}
			
	        for (int i = 1; i <=10; i++) {
				System.out.println("For loop --- Jeevika");
			}
			
			//While loop
			
			int a=1;
			while(a<=10) {
				System.out.println(a);
				a++;
			}
			
			int b=1;
			while(b<=5) {
				System.out.println("While loop --- Jeevika");
				b++;
			}
		}

	}


